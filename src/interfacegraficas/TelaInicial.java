    private void btnConectarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConectarActionPerformed
        try {
            ClienteDados.get().setNome(txtNome.getText());
            if (txtNome.getText().isEmpty())throw new Exception("Favor digitar nome");
            
            ClienteServidor.get().iniciarConexao(txtIP.getText());
            Dados jogador = new Dados();
            jogador.setAction(Dados.Action.CONECTAR);
            jogador.setNome(ClienteDados.get().getNome());
            ClienteServidor.get().setConectar(jogador);
            //Menu Personalizado

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
            JOptionPane.showMessageDialog(null, "NÃ£o foi possivel conectar ao servidor!", "Erro ao conectar", JOptionPane.ERROR_MESSAGE);
        }


    }//GEN-LAST:event_btnConectarActionPerformed

